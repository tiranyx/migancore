#!/usr/bin/env python3
"""
Auto Identity Eval Gate — Day 71e

Runs identity eval against current production model daily.
Logs results to SQLite + stdout.
Alerts if cosine sim < PASS_THRESHOLD.

Run manually:
    docker compose exec api python -m scripts.auto_eval_gate

Cron (daily 06:00 UTC):
    0 6 * * * cd /opt/ado && docker compose exec -T api python -m scripts.auto_eval_gate >> /var/log/migancore/eval.log 2>&1

Author: MiganCore ADO
"""
from __future__ import annotations

import argparse
import json
import sqlite3
import subprocess
import sys
from datetime import datetime, timezone
from pathlib import Path

# Config
DB_PATH = Path("/app/data/training/eval_history.db")
EVAL_SCRIPT = Path("/app/eval/run_identity_eval.py")
BASELINE = Path("/app/eval/baseline_day58.json")
PASS_THRESHOLD = 0.92
ALERT_THRESHOLD = 0.85
MODEL_TAG = "migancore:0.4"
RESULT_FILE = Path(f"/app/eval_result_{MODEL_TAG}.json")


def init_db():
    DB_PATH.parent.mkdir(parents=True, exist_ok=True)
    with sqlite3.connect(DB_PATH) as conn:
        conn.execute(
            """
            CREATE TABLE IF NOT EXISTS eval_runs (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                run_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                model_tag TEXT NOT NULL,
                weighted_avg REAL,
                simple_avg REAL,
                identity_gate TEXT,
                decision TEXT,
                pass_count INTEGER,
                total_prompts INTEGER,
                alert_triggered INTEGER DEFAULT 0,
                raw_json TEXT
            )
            """
        )
        conn.execute(
            "CREATE INDEX IF NOT EXISTS idx_eval_runs_date ON eval_runs(run_at)"
        )
        conn.commit()


def run_eval() -> dict:
    """Run identity eval and parse results from JSON output file."""
    cmd = [
        sys.executable, str(EVAL_SCRIPT),
        "--mode", "eval",
        "--reference", str(BASELINE),
        "--model-tag", MODEL_TAG,
    ]
    result = subprocess.run(cmd, capture_output=True, text=True, timeout=300)
    
    if result.returncode != 0:
        return {"error": result.stderr or result.stdout, "pass": False}
    
    # Read the JSON result file generated by run_identity_eval.py
    if RESULT_FILE.exists():
        with open(RESULT_FILE, "r", encoding="utf-8") as f:
            return json.load(f)
    
    # Fallback: try to parse from stdout last JSON line
    for line in reversed((result.stdout + result.stderr).splitlines()):
        line = line.strip()
        if not line:
            continue
        try:
            return json.loads(line)
        except json.JSONDecodeError:
            continue
    
    return {"error": "No result file or JSON output found", "pass": False}


def check_thresholds(summary: dict) -> tuple[str, bool, list[str]]:
    """Returns (decision, alert_triggered, alerts)."""
    alerts = []
    weighted_avg = summary.get("weighted_avg_cosine_sim", 0.0)
    identity_gate = summary.get("identity_hard_gate", "PASS")
    
    if weighted_avg < PASS_THRESHOLD:
        alerts.append(f"FAIL: weighted_avg {weighted_avg:.4f} < {PASS_THRESHOLD}")
    
    if weighted_avg < ALERT_THRESHOLD:
        alerts.append(f"ALERT: weighted_avg {weighted_avg:.4f} < {ALERT_THRESHOLD}")
    
    if identity_gate != "PASS":
        alerts.append(f"HARD GATE FAIL: identity_hard_gate = {identity_gate}")
    
    decision = summary.get("decision", "ROLLBACK")
    alert_triggered = decision == "ROLLBACK" or weighted_avg < ALERT_THRESHOLD
    
    return decision, alert_triggered, alerts


def log_run(summary: dict, alert_triggered: bool):
    with sqlite3.connect(DB_PATH) as conn:
        conn.execute(
            """
            INSERT INTO eval_runs 
            (model_tag, weighted_avg, simple_avg, identity_gate, decision, pass_count, total_prompts, alert_triggered, raw_json)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)
            """,
            (
                summary.get("model_tag", MODEL_TAG),
                summary.get("weighted_avg_cosine_sim", 0.0),
                summary.get("simple_avg_cosine_sim", 0.0),
                summary.get("identity_hard_gate", "UNKNOWN"),
                summary.get("decision", "ROLLBACK"),
                summary.get("passed", 0),
                summary.get("total_prompts", 0),
                1 if alert_triggered else 0,
                json.dumps(summary, default=str),
            ),
        )
        conn.commit()


def print_summary():
    with sqlite3.connect(DB_PATH) as conn:
        rows = conn.execute(
            "SELECT run_at, model_tag, weighted_avg, decision, alert_triggered FROM eval_runs ORDER BY run_at DESC LIMIT 7"
        ).fetchall()
    
    print("\n=== LAST 7 EVAL RUNS ===")
    print(f"{'Date':20s} | {'Model':20s} | {'Score':>7s} | {'Result':8s} | {'Alert':5s}")
    print("-" * 72)
    for r in rows:
        alert_flag = "🚨" if r[4] else " "
        print(f"{r[0]:20s} | {r[1]:20s} | {r[2]:>7.4f} | {r[3]:8s} | {alert_flag:5s}")
    print()


def main():
    parser = argparse.ArgumentParser(description="MiganCore Auto Identity Eval Gate")
    parser.add_argument("--summary", action="store_true", help="Print last 7 runs and exit")
    args = parser.parse_args()
    
    init_db()
    
    if args.summary:
        print_summary()
        return
    
    print(f"[{datetime.now(timezone.utc).isoformat()}] Starting auto-eval gate...")
    print(f"Model: {MODEL_TAG}")
    print(f"Baseline: {BASELINE}")
    print(f"Pass threshold: {PASS_THRESHOLD}")
    print(f"Alert threshold: {ALERT_THRESHOLD}")
    print()
    
    summary = run_eval()
    
    if "error" in summary:
        print(f"EVAL ERROR: {summary['error']}")
        sys.exit(1)
    
    decision, alert_triggered, alerts = check_thresholds(summary)
    
    print(f"Weighted avg: {summary.get('weighted_avg_cosine_sim', 0.0):.4f}")
    print(f"Simple avg:   {summary.get('simple_avg_cosine_sim', 0.0):.4f}")
    print(f"Identity gate: {summary.get('identity_hard_gate', 'UNKNOWN')}")
    print(f"Pass rate: {summary.get('passed', 0)}/{summary.get('total_prompts', 0)}")
    print(f"Decision: {decision}")
    
    if alerts:
        print("\nALERTS:")
        for a in alerts:
            print(f"  🚨 {a}")
    
    log_run(summary, alert_triggered)
    print(f"\nLogged to {DB_PATH}")
    
    if alert_triggered:
        print("\n⚠️  ALERT TRIGGERED — Identity may be drifting!")
        print("Action needed: Review training data, run SFT anchor reinforcement.")
        sys.exit(2)
    
    print("\n✅ Eval gate PASS. Identity stable.")


if __name__ == "__main__":
    main()
