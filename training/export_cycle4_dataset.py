async def fetch_from_db(sources: list[tuple[str, int, str]]) -> list[dict]:
    """Fetch pairs from Postgres via SQLAlchemy async session."""
    from sqlalchemy import text
    import models.base as _base
    from models.base import init_engine

    init_engine()

    rows_all: list[dict] = []

    async with _base.AsyncSessionLocal() as session:
        for source_pattern, limit, label in sources:
            use_like = "%" in source_pattern
            if use_like:
                q = text("""
                    SELECT prompt, chosen, rejected, source_method, judge_score
                    FROM preference_pairs
                    WHERE source_method LIKE :pattern
                    ORDER BY judge_score DESC NULLS LAST, created_at ASC
                    LIMIT :limit
                """)
            else:
                q = text("""
                    SELECT prompt, chosen, rejected, source_method, judge_score
                    FROM preference_pairs
                    WHERE source_method = :pattern
                    ORDER BY judge_score DESC NULLS LAST, created_at ASC
                    LIMIT :limit
                """)

            res = await session.execute(q, {"pattern": source_pattern, "limit": limit})
            fetched = res.fetchall()

            for row in fetched:
                rows_all.append({
                    "prompt":   row[0],
                    "chosen":   row[1],
                    "rejected": row[2],
                    "_source":  row[3],
                    "_score":   float(row[4]) if row[4] is not None else None,
                    "_label":   label,
                })
            print(f"  {label:20s}: {len(fetched):3d} pairs  (pattern: {source_pattern})", flush=True)

    return rows_all


def read_new_pairs_from_jsonl(path: str) -> list[dict]:
    """Read new pairs from JSONL file (generate_cycle4_dataset.py --export output)."""
    p = Path(path)
    if not p.exists():
        print(f"ERROR: new-pairs file not found: {path}")
        return []

    pairs: list[dict] = []
    with p.open(encoding="utf-8") as f:
        for line_no, line in enumerate(f, 1):
            line = line.strip()
            if not line:
                continue
            try:
                obj = json.loads(line)
                if "prompt" not in obj or "chosen" not in obj or "rejected" not in obj:
                    print(f"  WARN line {line_no}: missing required fields, skipping")
                    continue
                # Add label from source_method if present
                obj.setdefault("_label", obj.get("source_method", "cycle4_new").split(":")[0])
                pairs.append(obj)
            except json.JSONDecodeError as e:
                print(f"  WARN line {line_no}: JSON parse error: {e}")

    print(f"  [new-pairs file]: {len(pairs)} pairs read from {path}")
    return pairs


def validate_and_deduplicate(samples: list[dict]) -> tuple[list[dict], dict]:
    """Dedup by prompt prefix + validate chosen quality."""
    # Deduplicate
    seen: set[str] = set()
    deduped: list[dict] = []
    dupe_count = 0
    for s in samples:
        key = s["prompt"][:120]
        if key not in seen:
            seen.add(key)
            deduped.append(s)
        else:
            dupe_count += 1

    # Validate chosen — flag if bad identity patterns
    identity_warns: list[dict] = []
    empty_warns: list[dict] = []
    short_chosen_warns: list[dict] = []

    for s in deduped:
        chosen = s.get("chosen", "")
        rejected = s.get("rejected", "")

        # Empty check
        if not chosen or not rejected:
            empty_warns.append(s)

        # Short chosen (< 30 chars = likely a failed generation)
        if len(chosen) < 30:
            short_chosen_warns.append(s)

        # Identity contamination (model claiming to be other AI)
        for bad in CHOSEN_REJECT_PATTERNS:
            if bad.lower() in chosen.lower():
                identity_warns.append(s)
                break

    stats = {
        "total_after_dedup": len(deduped),
        "duplicates_removed": dupe_count,
        "identity_contamination": len(identity_warns),
        "empty_fields": len(empty_warns),
        "short_chosen": len(short_chosen_warns),
    }

    if identity_warns:
        print(f"  WARN: {len(identity_warns)} chosen responses have identity contamination:")
        for w in identity_warns[:3]:
            print(f"    [{w.get('_source','')}] chosen: {w['chosen'][:80]}")

    if short_chosen_warns:
        print(f"  WARN: {len(short_chosen_warns)} chosen < 30 chars (likely failed generation)")

    if empty_warns:
        print(f"  ERROR: {len(empty_warns)} pairs with empty chosen/rejected — will be filtered")
        deduped = [s for s in deduped if s.get("chosen") and s.get("rejected")]

    return deduped, stats


def source_breakdown(samples: list[dict]) -> dict[str, int]:
    """Count pairs per label."""
    breakdown: dict[str, int] = {}
    for s in samples:
        label = s.get("_label", "unknown")
        breakdown[label] = breakdown.get(label, 0) + 1
    return breakdown


async def export_cycle4(
    output_path: str | None,
    new_pairs_file: str | None = None,
    dry_run: bool = False,
    shuffle: bool = True,
) -> dict:
    """Main export logic. Returns summary dict."""

    print("\n[1/4] Fetching curated pairs from DB...")
    curated = await fetch_from_db(CURATED_SOURCES)
    print(f"  Curated total: {len(curated)}")

    print("\n[2/4] Fetching/loading new Cycle 4 pairs...")
    if new_pairs_file:
        new_pairs = read_new_pairs_from_jsonl(new_pairs_file)
    else:
        # Try to load from DB (if generate_cycle4_dataset.py --store-db was used)
        new_pairs = await fetch_from_db(NEW_CYCLE4_SOURCES)
        if not new_pairs:
            print("  WARNING: No Cycle 4 pairs found in DB.")
            print("  Run generate_cycle4_dataset.py first, or use --new-pairs to specify a JSONL file.")

    print(f"  New pairs total: {len(new_pairs)}")

    # Combine
    all_samples = curated + new_pairs
    print(f"\n[3/4] Combined: {len(all_samples)} pairs total")

    # Validate + dedup
    validated, stats = validate_and_deduplicate(all_samples)
    print(f"  After validation: {stats['total_after_dedup']} pairs "
          f"({stats['duplicates_removed']} dupes removed)")

    if shuffle:
        random.seed(42)  # deterministic shuffle for reproducibility
        random.shuffle(validated)

    # Print breakdown
    breakdown = source_breakdown(validated)
    print("\n  By label:")
    total_curated = sum(v for k, v in breakdown.items()
                        if k in {"identity", "tool_use", "code", "cai", "distill"})
    total_new = sum(v for k, v in breakdown.items()
                    if k not in {"identity", "tool_use", "code", "cai", "distill"})
    for label in sorted(breakdown, key=lambda x: -breakdown[x]):
        pct = breakdown[label] / len(validated) * 100
        print(f"    {label:20s} {breakdown[label]:4d}  ({pct:.1f}%)")
    print(f"  ─────────────────────────────────")
    print(f"  {'CURATED':20s} {total_curated:4d}")
    print(f"  {'NEW CYCLE 4':20s} {total_new:4d}")
    print(f"  {'TOTAL':20s} {len(validated):4d}")

    # KPI check
    target_min, target_max = 680, 800
    kpi_pass = target_min <= len(validated) <= target_max
    print(f"\n  KPI: {target_min}≤pairs≤{target_max} → {'✅ PASS' if kpi_pass else '❌ FAIL'}")
    print(f"  New category coverage:")
    # Labels from generate_cycle4_dataset.py use source_method prefix as label
    # e.g. "evolution_anchor_v1:cycle4" → split(":")[0] = "evolution_anchor_v1"
    evo   = breakdown.get('evolution', 0) or breakdown.get('evolution_anchor_v1', 0)
    creat = breakdown.get('creative', 0) or breakdown.get('creative_anchor_v1', 0)
    tdisk = breakdown.get('tool_disc', 0) or breakdown.get('tool_discriminate_v1', 0)
    voice = breakdown.get('voice', 0) or breakdown.get('voice_natural_v1', 0)
    print(f"    evolution_aware  : {evo:3d} (target ≥ 30)  {'✅' if evo >= 30 else '❌'}")
    print(f"    creative         : {creat:3d} (target ≥ 40)  {'✅' if creat >= 40 else '❌'}")
    print(f"    tool_disc        : {tdisk:3d} (target ≥ 40)  {'✅' if tdisk >= 40 else '❌'}")
    print(f"    voice            : {voice:3d} (target ≥ 30)  {'✅' if voice >= 30 else '❌'}")

    summary = {
        "total": len(validated),
        "by_label": breakdown,
        "curated_pairs": total_curated,
        "new_cycle4_pairs": total_new,
        "kpi_pass": kpi_pass,
        "validation": stats,
        "output": output_path,
    }

    if dry_run:
        print("\n[4/4] DRY RUN — file not written.")
        return summary

    # Write JSONL
    print(f"\n[4/4] Writing to {output_path}...")
    out = Path(output_path)
    out.parent.mkdir(parents=True, exist_ok=True)
    with out.open("w", encoding="utf-8") as f:
        for s in validated:
            clean = {k: v for k, v in s.items() if not k.startswith("_")}
            f.write(json.dumps(clean, ensure_ascii=False) + "\n")

    size_kb = out.stat().st_size / 1024
    print(f"  Written: {len(validated)} lines, {size_kb:.1f} KB")
    print(f"  Path: {out.resolve()}")

    return summary


def main():
    parser = argparse.ArgumentParser(description="Export Cycle 4 combined training dataset")
    parser.add_argument("--output", default="/app/workspace/cycle4_combined_dataset.jsonl",
                        help="Output JSONL path (default: /app/workspace/cycle4_combined_dataset.jsonl)")
    parser.add_argument("--new-pairs", default=None,
                        help="Path to JSONL file with new Cycle 4 pairs "
                             "(from generate_cycle4_dataset.py --export). "
                             "If omitted, reads from DB (requires --store-db was used).")
    parser.add_argument("--dry-run", action="store_true",
                        help="Print stats but do not write output file")
    parser.add_argument("--no-shuffle", action="store_true",
                        help="Disable random shuffle (default: shuffle with seed=42)")
    args = parser.parse_args()

    print("=" * 60)
    print("MiganCore Cycle 4 Dataset Export")
    print("=" * 60)
    print(f"Output   : {args.output}")
    print(f"New pairs: {args.new_pairs or '(from DB)'}")
    print(f"Dry run  : {args.dry_run}")
    print(f"Shuffle  : {not args.no_shuffle}")

    summary = asyncio.run(export_cycle4(
        output_path=args.output,
        new_pairs_file=args.new_pairs,
        dry_run=args.dry_run,
        shuffle=not args.no_shuffle,
    ))

    print("\n" + "=" * 60)
    print("EXPORT COMPLETE")
    print("=" * 60)
    print(f"Total pairs : {summary['total']}")
    print(f"Curated     : {summary['curated_pairs']}")
    print(f"New Cycle 4 : {summary['new_cycle4_pairs']}")
    print(f"KPI pass    : {'✅' if summary['kpi_pass'] else '❌'}")
    if not args.dry_run:
        print(f"Output file : {summary['output']}")
    print()
    if summary["kpi_pass"]:
        print("Next step: Run cycle4_orpo_vast.py to launch Vast.ai training.")
        print("  python3 /opt/ado/training/cycle4_orpo_vast.py")
    else:
        print("KPI FAIL — check pair counts above. May need to re-run generator.")


if __name__ == "__main__":
    main()
